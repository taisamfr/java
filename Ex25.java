/******************************************************************************

25. Faça um programa, utilizando while, que mostre na tela os números de 0 a 100.

*******************************************************************************/
public class Ex25
{
	public static void main(String[] args) {
	    int numero = 1;
	    
	    while (numero <= 100) {
	        System.out.println(numero);
	        numero++;
	    }
	}
}